// In JavaScript müssen Variablen mit let, const oder var deklariert werden.
// Der Datentyp wird ebenfalls automatisch erkannt.

// Ein Wahrheitswert (Boolean) zuweisen:
let is_happy = true;
console.log("Wert von is_happy:", is_happy);
console.log("Typ von is_happy:", typeof is_happy);
//
// // Eine Ganzzahl (Integer) zuweisen:
let x = 10;
console.log("Wert von x:", x);
console.log("Typ von x:", typeof x);
//
// // Eine Fließkommazahl (float) zuweisen:
let pi = 3.14159;
console.log("Wert von pi:", pi);
console.log("Typ von pi:", typeof pi);
//
// // Einen String zuweisen:
let name = "John";
console.log("Wert von name:", name);
console.log("Typ von name:", typeof name);

