// Rechenoperatoren
console.log(3 + 7);  // 10
console.log(13 - 7);  // 6
console.log(3 * 7);  // 21
console.log(21 / 7);  // 3
console.log("3" + "7");  // 37
console.log("3" + 7);  // 37
console.log(3 + "7");  // 37

// Modulo - Restwert-Division
console.log(37 % 7);  // 2
console.log(7 % 7);  // 0
console.log(4 % 7);  // 4

// Zuweisungsoperator
let x = 7;
console.log(x + 1); // dadurch hat sich x nicht erhöht
console.log(x);  // 7

x += 1; // jetzt hat sich x erhöht
console.log(x);  // 8
x -= 1;
x -= 1;
console.log(x);  // 6




